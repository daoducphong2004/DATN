                        mất khỏi đây. Nếu tôi để chuyện này khiến tôi bị nghi ngờ, tôi thực sự sẽ chỉ còn cách cái
                        chết có năm giây.</p>
                    <p id="20">Ngay sau đó, cánh cửa lớp học bật mở. Mái tóc hơi dài ánh màu bạc, phất phơ
